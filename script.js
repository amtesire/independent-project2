$(document).ready(function(){
    $(".wor").hide();
    $("#work").css({
     opacity:1
   });
    $("#wo").hover(function(){
      $(this).stop().css({opacity:.4});
      $(".wor").fadeIn();
      }, function(){
        $(this).stop().css({opacity:1})
      $(".wor").fadeOut();
    });
  });
